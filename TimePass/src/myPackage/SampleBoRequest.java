package myPackage;

public class SampleBoRequest {

	private String asOfDate;
	private String providerId;
	private String carrierId;
	private String listId;
	private String networkServiceType;
	private String state;
	private String feeOverrideType;
	private String feeDispeningFeeType;
	private String rateType;
	private String subType;

	public String getAsOfDate() {
		return asOfDate;
	}

	public void setAsOfDate(String asOfDate) {
		this.asOfDate = asOfDate;
	}

	public String getProviderId() {
		return providerId;
	}

	public void setProviderId(String providerId) {
		this.providerId = providerId;
	}

	public String getCarrierId() {
		return carrierId;
	}

	public void setCarrierId(String carrierId) {
		this.carrierId = carrierId;
	}

	public String getListId() {
		return listId;
	}

	public void setListId(String listId) {
		this.listId = listId;
	}

	public String getNetworkServiceType() {
		return networkServiceType;
	}

	public void setNetworkServiceType(String networkServiceType) {
		this.networkServiceType = networkServiceType;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getFeeOverrideType() {
		return feeOverrideType;
	}

	public void setFeeOverrideType(String feeOverrideType) {
		this.feeOverrideType = feeOverrideType;
	}

	public String getFeeDispeningFeeType() {
		return feeDispeningFeeType;
	}

	public void setFeeDispeningFeeType(String feeDispeningFeeType) {
		this.feeDispeningFeeType = feeDispeningFeeType;
	}

	public String getRateType() {
		return rateType;
	}

	public void setRateType(String rateType) {
		this.rateType = rateType;
	}

	public String getSubType() {
		return subType;
	}

	public void setSubType(String subType) {
		this.subType = subType;
	}

}
