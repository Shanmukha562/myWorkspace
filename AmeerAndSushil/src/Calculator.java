
public class Calculator {

	public int  x;
	public int y;
	
	
	public int Addition(int firstNumber, int secondNumber){
		x = firstNumber;
		y = secondNumber;
		return x + y;
	}
	
	public int Subraction(int firstNumber, int secondNumber){
         x = firstNumber;
         y = secondNumber;
		return x - y;
	}
}
